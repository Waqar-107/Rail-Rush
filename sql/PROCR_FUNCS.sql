/*ADD REV OF FREIGHT*/
CREATE OR REPLACE 
PROCEDURE "ADD_FREIGHT_REV" (TRID  INTEGER)
AS
	MCNT FLOAT;
	INC INTEGER;
	TDATE VARCHAR2(15);
BEGIN
	TDATE:=TO_CHAR(SYSDATE,'YYYY-MM-DD');
	SELECT PRICE INTO INC FROM FARE WHERE TRAIN_ID=TRID;

	SELECT EARNING INTO MCNT 
	FROM REVENUE 
	WHERE RDAY=TDATE AND TRAIN_ID=TRID;

	UPDATE REVENUE SET EARNING=MCNT+INC
	WHERE RDAY=TDATE AND TRAIN_ID=TRID;

EXCEPTION
	WHEN NO_DATA_FOUND THEN
		INSERT INTO REVENUE VALUES(TDATE,INC,TRID);
END;

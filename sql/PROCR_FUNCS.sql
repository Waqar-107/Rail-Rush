/*AFTER GIVING A COMPANY TENDER, DELETE THEM FROM OFFER AND STORE IN HISTORY*/
CREATE OR REPLACE 
PROCEDURE "DEAL_WITH_TENDER" (TID  INTEGER, COM  VARCHAR2)
AS
	DES VARCHAR2(250);
	TDATE DATE;
	MAIL VARCHAR2(50);
	MCOSTING INTEGER;
	CID INTEGER;
BEGIN

	SELECT COMPANY_ID,EMAIL_ID 
	INTO CID,MAIL
	FROM COMPANY WHERE CNAME=COM;

	SELECT T1.EXP_TIME,T2.COSTING,T1.DESCRIPTION
	INTO TDATE,MCOSTING,DES
	FROM TENDER_DES T1 
	JOIN TENDER_OFFER T2 ON T1.TENDER_ID=T2.TENDER_ID
	WHERE TID=T2.TENDER_ID AND T2.C_ID=CID;


	INSERT INTO TENDER_HISTORY VALUES(TID,COM,MAIL,TDATE,MCOSTING,DES);
	DELETE FROM TENDER_DES WHERE TENDER_ID=TID;
END;


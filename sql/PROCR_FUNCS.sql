/*update the trips*/
CREATE OR REPLACE 
PROCEDURE "UPDATE_ALL_TRIPS" (TID  INTEGER, OT  VARCHAR2, NT  VARCHAR2)
AS
	T_OT DATE;
	T_NT DATE;
DIFF INTEGER;
BEGIN
	T_OT := TO_DATE(OT,'YYYY-MM-DD');
	T_NT := TO_DATE(NT,'YYYY-MM-DD');

	DIFF := T_NT-T_OT;

	FOR VAR IN (SELECT TRIP_DATE,TRIP_ID FROM TRIP WHERE TRIP_ID>=TID)
	LOOP
		UPDATE TRIP 
		SET TRIP_DATE=VAR.TRIP_DATE+DIFF, UPDATED=1
		WHERE TRIP_ID=VAR.TRIP_ID;
	END LOOP;
	
END;


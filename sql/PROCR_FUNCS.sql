/*BOOKING HISTORY*/
CREATE OR REPLACE 
PROCEDURE "BHISTORY" (PID  INTEGER, TRID  INTEGER)
AS
	X INTEGER;
BEGIN
	SELECT CNT INTO X FROM BOOKING_HISTORY
	WHERE PASSENGER_ID=PID AND TRAIN_ID=TRID;
	UPDATE BOOKING_HISTORY
	SET CNT=X+1 
	WHERE PASSENGER_ID=PID AND TRAIN_ID=TRID;
EXCEPTION
	WHEN NO_DATA_FOUND THEN
		INSERT INTO BOOKING_HISTORY VALUES(PID,TRID,1);
END;


